public class Treasure {

	private String name;
	private float weight;
	private int value;

	public void set_name(String n)
	{
		name = n;
	}

	public void set_weight(float w)
	{
		weight = w;
	}

	public void set_value(int v)
	{
		value = v;
	}

	public String get_name()
	{
		return name;
	}

	public float get_weight()
	{
		return weight;
	}

	public int get_value()
	{
		return value;
	}

}
